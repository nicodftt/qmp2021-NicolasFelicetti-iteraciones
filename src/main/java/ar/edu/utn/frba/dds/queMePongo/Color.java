package ar.edu.utn.frba.dds.queMePongo;

public class Color {

    private int red;
    private int green;
    private int blue;

    public Color(int _red,int _green,int _blue){

        this.red = _red;
        this.green = _green;
        this.blue = _blue;
    }

    public int getRed(){
        return this.red;
    }

    public int getGreen(){

        return this.green;
    }

    public int getBlue(){

        return this.blue;
    }
}
